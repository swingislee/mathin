import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const databaseUrl = process.env.DATABASE_URL;
if (!databaseUrl) {
  console.error("DATABASE_URL is required for db:types");
  process.exit(2);
}
const command = process.platform === "win32" ? "supabase.exe" : "supabase";
const result = spawnSync(command, ["gen", "types", "typescript", "--db-url", databaseUrl, "--schema", "public"], { encoding: "utf8", shell: false });
if (result.error?.code === "ENOENT") {
  console.error("Supabase CLI is required. Install the official CLI outside this repository, then rerun pnpm db:types.");
  process.exit(2);
}
if (result.status !== 0) {
  process.stderr.write(result.stderr || "supabase gen types failed\n");
  process.exit(result.status ?? 1);
}
if (!result.stdout.includes("export type Database")) {
  console.error("Generated output did not contain a Database type");
  process.exit(1);
}
const target = path.join(process.cwd(), "src", "lib", "database.types.ts");
fs.writeFileSync(target, `// Generated by pnpm db:types. Do not edit manually.\n${result.stdout}`, "utf8");
console.log(`Wrote ${path.relative(process.cwd(), target)}`);
