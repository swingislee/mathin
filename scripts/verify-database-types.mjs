import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const target = path.join(root, "src", "lib", "database.types.ts");
if (!fs.existsSync(target)) {
  console.error("Database types missing. Run DATABASE_URL=... pnpm db:types after applying migrations.");
  process.exit(1);
}
const text = fs.readFileSync(target, "utf8");
if (!text.includes("Generated by pnpm db:types") || !text.includes("export type Database")) {
  console.error("src/lib/database.types.ts is not a valid generated type file");
  process.exit(1);
}
const migrationDir = path.join(root, "supabase", "migrations");
const latestMigration = Math.max(...fs.readdirSync(migrationDir).filter((name) => name.endsWith(".sql")).map((name) => fs.statSync(path.join(migrationDir, name)).mtimeMs));
if (fs.statSync(target).mtimeMs < latestMigration) {
  console.error("Database types are older than the latest migration. Regenerate them from the migrated database.");
  process.exit(1);
}
console.log("Database generated types are present and newer than migrations");
