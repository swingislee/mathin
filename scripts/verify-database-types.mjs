import fs from "node:fs";
import path from "node:path";
import { DIGEST_PREFIX, migrationsDigest } from "./lib/migrations-digest.mjs";

const root = process.cwd();
const target = path.join(root, "src", "lib", "database.types.ts");
if (!fs.existsSync(target)) {
  console.error("Database types missing. Run DATABASE_URL=... pnpm db:types after applying migrations.");
  process.exit(1);
}
const text = fs.readFileSync(target, "utf8");
if (!text.includes("Generated by pnpm db:types") || !text.includes("export type Database")) {
  console.error("src/lib/database.types.ts is not a valid generated type file");
  process.exit(1);
}
const recorded = text.split("\n").find((line) => line.startsWith(DIGEST_PREFIX))?.slice(DIGEST_PREFIX.length).trim();
if (!recorded) {
  console.error("Database types carry no migrations digest. Regenerate them with pnpm db:types.");
  process.exit(1);
}
const current = migrationsDigest(root);
if (recorded !== current) {
  console.error(
    `Database types are stale: recorded migrations digest ${recorded.slice(0, 12)} != current ${current.slice(0, 12)}.\n` +
      "Apply the migrations and regenerate the types from the migrated database (pnpm db:types).",
  );
  process.exit(1);
}
console.log("Database generated types match the current migrations");
